#include <stdio.h>

int main()
{
    int a,m=0;
    scanf("%d",&a);
    if(a>1000000)m+=(a-1000000)*0.01;
    if(a>600000)m+=((a<1000000?a:1000000)-600000)*0.015;
    if(a>400000)m+=((a<600000?a:600000)-400000)*0.03;
    if(a>200000)m+=((a<400000?a:400000)-200000)*0.05;
    if(a>100000)m+=((a<200000?a:200000)-100000)*0.075;
    m+=(a<=100000?a:100000)*0.1;
    printf("%d\n",m);
    return 0;
}